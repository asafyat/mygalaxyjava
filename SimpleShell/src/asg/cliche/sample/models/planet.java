package asg.cliche.sample.models;
public class planet {
	String idPlayer;
	int IdSolarSystem;
	String name;
	int level;
	public String getIdPlayer() {
		return idPlayer;
	}
	public void setIdPlayer(String idPlayer) {
		this.idPlayer = idPlayer;
	}
	public int getIdSolarSystem() {
		return IdSolarSystem;
	}
	public void setIdSolarSystem(int idSolarSystem) {
		IdSolarSystem = idSolarSystem;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
}
